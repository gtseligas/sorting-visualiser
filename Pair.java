package sortingVisualiserPackage;

//Simple class to implement integer pair, as I couldn't find a Java pair
public class Pair {
	private int first;
	private int second;
	public Pair(int a, int b) {
		first = a;
		second = b;
	}
	public int getFirst() {return first;}
	public int getSecond() {return second;}		
}
